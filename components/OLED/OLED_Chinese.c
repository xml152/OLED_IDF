#define CHINESE_FONT_COUNT (sizeof(chinese_font) / sizeof(chinese_font[0]))

// 查找汉字索引
static int find_chinese_index(const char* str) {
    for (int i = 0; i < CHINESE_FONT_COUNT; i++) {
        if (memcmp(str, chinese_font[i].utf8_char, 3) == 0) {
            return i;
        }
    }
    return -1;
}

// 绘制 16x16 汉字
void OLED_DrawChinese16x16(uint8_t x, uint8_t y, const char* str, bool color) {
    int idx = find_chinese_index(str);
    if (idx < 0) return;  // 汉字不在字库中
    
    const uint8_t* font = chinese_font[idx].data;
    
    for (uint8_t row = 0; row < 16; row++) {
        uint8_t high = font[row * 2];
        uint8_t low = font[row * 2 + 1];
        
        for (uint8_t col = 0; col < 8; col++) {
            bool pixel = (high >> (7 - col)) & 0x01;
            OLED_DrawPixel(x + col, y + row, color ? pixel : !pixel);
        }
        for (uint8_t col = 0; col < 8; col++) {
            bool pixel = (low >> (7 - col)) & 0x01;
            OLED_DrawPixel(x + 8 + col, y + row, color ? pixel : !pixel);
        }
    }
}

// 绘制中文字符串
void OLED_DrawChineseString(uint8_t x, uint8_t y, const char* str, bool color) {
    uint8_t x_pos = x;
    
    while (*str) {
        // 检查是否是 UTF-8 中文字符（首字节 >= 0xE0）
        if ((uint8_t)*str >= 0xE0) {
            // UTF-8 中文占 3 字节
            if (x_pos + 16 <= OLED_WIDTH) {
                OLED_DrawChinese16x16(x_pos, y, str, color);
                x_pos += 16;
            }
            str += 3;
        } else {
            // ASCII 字符，使用原来的字体
            if (x_pos + 6 <= OLED_WIDTH) {
                OLED_DrawChar(x_pos, y, *str, color);
                x_pos += 6;
            }
            str++;
        }
    }
}
